export const approvedImportedBeachAssets = importedBeachAssets.filter(
  (asset) => asset.qualityStatus === 'approved' && asset.approvedForLibraryPreview,
)

export const quarantinedImportedBeachAssets = importedBeachAssets.filter(
  (asset) => asset.qualityStatus === 'quarantined',
)

export function getImportedBeachAssets(category: ImportedBeachAssetCategory): ImportedBeachAsset[] {
  return approvedImportedBeachAssets.filter((asset) => asset.category === category)
}

export function getImportedBeachAssetCounts(category: ImportedBeachAssetCategory): number {
  return getImportedBeachAssets(category).length
}
