export type BeachLibraryCategoryId =
  | 'umbrellas'
  | 'palms'
  | 'furniture'
  | 'map_items'
  | 'walkways'
  | 'zones'
  | 'icons_symbols'

export type BeachLibraryItemStatus = 'available' | 'planned'

export interface BeachLibraryCategory {
  id: BeachLibraryCategoryId
  label: string
  description: string
  status: BeachLibraryItemStatus
  availableCount: number
  plannedCount: number
  previewKind: string
  previewUrl?: string
}

export interface BeachLibraryItem {
  id: string
  categoryId: BeachLibraryCategoryId
  label: string
  description: string
  status: BeachLibraryItemStatus
  defaultWidthM?: number
  defaultHeightM?: number
  moduleLengthM?: number
  collisionShape?: 'circle' | 'rect' | 'polygon'
  previewKind: string
  previewUrl?: string
  source?: string
  license?: string
  requiresAttribution?: boolean
  qualityStatus?: 'approved' | 'quarantined' | 'rejected' | 'planned'
  visualStyle?: 'top_down' | 'isometric_light' | 'technical_icon' | 'flat_icon' | 'mixed_unknown'
  qualityReason?: string
  palmScaleGroup?: 'large' | 'medium' | 'small'
}

